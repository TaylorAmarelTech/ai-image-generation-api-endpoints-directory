# ---------------------------------------------------------------------------
# Status emoji mapping
# ---------------------------------------------------------------------------

STATUS_EMOJI = {
    "working": "\u2705",
    "reachable": "\U0001f7e1",
    "auth_missing": "\U0001f511",
    "auth_failed": "\U0001f534",
    "needs_credits": "\U0001f4b3",
    "rate_limited": "\u23f3",
    "timeout": "\u23f1\ufe0f",
    "error": "\u274c",
    "offline": "\U0001f4a4",
    "skipped": "\u23ed\ufe0f",
    "unknown": "\u2753",
}

# Friendly tier labels
TIER_LABELS = {
    Tier.free: "Tier 1 \u2013 Truly Free",
    Tier.generous_free: "Tier 2 \u2013 Generous Free Tier",
    Tier.free_credits: "Tier 3 \u2013 Free Credits on Signup",
    Tier.freemium: "Tier 4 \u2013 Freemium",
    Tier.payg: "Tier 5 \u2013 Pay-per-Use",
    Tier.router: "Routers / Aggregators",
    Tier.local: "Local / Self-Hosted",
}

TIER_ORDER = [
    Tier.free,
    Tier.generous_free,
    Tier.free_credits,
    Tier.freemium,
    Tier.payg,
    Tier.router,
    Tier.local,
]

# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------


def generate_report(
    scan_results: list[ScanResult] | None = None,
    output_file: str = "README.md",
) -> str:
    """Generate a full README.md and write it to *output_file*.

    Parameters
    ----------
    scan_results:
        Optional list of :class:`ScanResult` objects from the most recent
        provider scan.  When provided the README will include live status
        indicators next to each provider.
    output_file:
        Destination path for the generated file (default ``README.md``).

    Returns
    -------
    str
        The generated markdown text.
    """
    # Index scan results by provider name for O(1) lookups
    results_map: dict[str, ScanResult] = {}
    if scan_results:
        for r in scan_results:
            results_map[r.provider_name] = r

    providers = get_providers()

    sections: list[str] = [
        _generate_header(),
        _generate_toc(),
        _generate_why_section(),
        _generate_quick_start(),
        _generate_curl_examples(),
        _generate_provider_overview(providers, results_map),
    ]

    # Per-tier detailed sections
    for tier in TIER_ORDER:
        tier_providers = [p for p in providers if p.tier == tier]
        if tier_providers:
            sections.append(
                _generate_tier_section(tier, tier_providers, results_map)
            )

    sections.extend([
        _generate_status_legend(),
        _generate_project_structure(),
        _generate_setup(),
        _generate_cli_reference(),
        _generate_cross_link(),
        _generate_license(),
        _generate_credits(),
    ])

    readme = "\n\n".join(sections) + "\n"

    Path(output_file).write_text(readme, encoding="utf-8")
    return readme


