def _generate_provider_overview(
    providers: list[Provider],
    results: dict[str, ScanResult],
) -> str:
    """Summary table of all providers with optional live status."""
    lines = [
        "## \U0001f5c2\ufe0f Provider Overview",
        "",
    ]

    # Build header
    has_status = bool(results)
    header_cols = ["Provider", "Tier", "Free Limits"]
    if has_status:
        header_cols.append("Status")
    sep_cols = ["---", "---", "---"]
    if has_status:
        sep_cols.append(":---:")

    lines.append("| " + " | ".join(header_cols) + " |")
    lines.append("| " + " | ".join(sep_cols) + " |")

    for p in providers:
        tier_label = TIER_LABELS.get(p.tier, str(p.tier))
        free_limits = p.free_limits or "\u2014"
        row = [f"**{p.name}**", tier_label, free_limits]
        if has_status:
            sr = results.get(p.name)
            if sr:
                emoji = STATUS_EMOJI.get(sr.status, STATUS_EMOJI["unknown"])
                row.append(f"{emoji} {sr.status}")
            else:
                row.append(STATUS_EMOJI["skipped"] + " skipped")
        lines.append("| " + " | ".join(row) + " |")

    return "\n".join(lines)


def _generate_tier_section(
    tier: Tier,
    providers: list[Provider],
    results: dict[str, ScanResult],
) -> str:
    """Detailed table for a single tier."""
    label = TIER_LABELS.get(tier, str(tier))
    lines = [
        f"## \U0001f3af {label}",
        "",
    ]

    has_status = bool(results)
    header_cols = ["Provider", "Endpoint", "Free Limits", "Models", "Capabilities"]
    if has_status:
        header_cols.append("Status")
    sep_cols = ["---", "---", "---", "---", "---"]
    if has_status:
        sep_cols.append(":---:")

    lines.append("| " + " | ".join(header_cols) + " |")
    lines.append("| " + " | ".join(sep_cols) + " |")

    for p in providers:
        name_cell = f"[{p.name}]({p.signup_url})" if p.signup_url else p.name
        endpoint = f"`{p.endpoint}`" if p.endpoint else "\u2014"
        free_limits = p.free_limits or "\u2014"
        models = ", ".join(p.models[:3]) if p.models else "\u2014"
        if p.models and len(p.models) > 3:
            models += f" +{len(p.models) - 3} more"
        capabilities = ", ".join(p.capabilities) if p.capabilities else "txt2img"

        row = [name_cell, endpoint, free_limits, models, capabilities]

        if has_status:
            sr = results.get(p.name)
            if sr:
                emoji = STATUS_EMOJI.get(sr.status, STATUS_EMOJI["unknown"])
                latency_str = ""
                if sr.latency_ms is not None:
                    latency_str = f" ({sr.latency_ms:.0f}ms)"
                row.append(f"{emoji}{latency_str}")
            else:
                row.append(STATUS_EMOJI["skipped"])

        lines.append("| " + " | ".join(row) + " |")

    return "\n".join(lines)


def _generate_status_legend() -> str:
    """Explain what each status value means."""
    lines = [
        "## \U0001f6a6 Status Legend",
        "",
        "| Symbol | Status | Meaning |",
        "| :---: | --- | --- |",
    ]
    descriptions = {
        "working": "Endpoint returned a valid image",
        "reachable": "Endpoint responded but image was not verified",
        "auth_missing": "API key required but not configured",
        "auth_failed": "API key was rejected by the provider",
        "needs_credits": "Account has no remaining credits",
        "rate_limited": "Too many requests \u2013 try again later",
        "timeout": "Request timed out",
        "error": "Endpoint returned an error",
        "offline": "Endpoint is unreachable",
        "skipped": "Provider was not scanned",
        "unknown": "Status could not be determined",
    }
    for status, emoji in STATUS_EMOJI.items():
        desc = descriptions.get(status, "")
        lines.append(f"| {emoji} | `{status}` | {desc} |")

    return "\n".join(lines)


