def _generate_header() -> str:
    """Title, version badge, and key metric badges."""
    providers = get_providers()
    total = len(providers)
    free_count = len([p for p in providers if p.tier in (Tier.free, Tier.generous_free)])

    lines = [
        "<!-- AUTO-GENERATED by report_generator.py \u2013 do not edit manually -->",
        "",
        "# \U0001f3a8 AI Image Generation API Directory",
        "",
        (
            f"![Version](https://img.shields.io/badge/version-{__version__}-blue) "
            f"![Providers](https://img.shields.io/badge/providers-{total}-green) "
            f"![Free](https://img.shields.io/badge/free-{free_count}-brightgreen)"
        ),
        "",
        "> **The most comprehensive directory of free & affordable AI image generation APIs**",
    ]
    return "\n".join(lines)


def _generate_toc() -> str:
    """Collapsible table of contents."""
    toc_items = [
        ("Why This Exists", "#-why-this-exists"),
        ("Quick Start (Python)", "#-quick-start-python"),
        ("Quick Start (cURL)", "#-quick-start-curl"),
        ("Provider Overview", "#-provider-overview"),
    ]
    for tier in TIER_ORDER:
        label = TIER_LABELS[tier]
        anchor = "#-" + label.lower().replace(" ", "-").replace("/", "").replace("\u2013", "").replace("--", "-").strip("-")
        toc_items.append((label, anchor))

    toc_items.extend([
        ("Status Legend", "#-status-legend"),
        ("Project Structure", "#-project-structure"),
        ("Setup", "#-setup"),
        ("CLI Reference", "#-cli-reference"),
        ("Sister Project \u2013 LLM APIs", "#-sister-project--llm-apis"),
        ("License", "#-license"),
        ("Credits", "#-credits"),
    ])

    lines = [
        "<details>",
        "<summary><strong>Table of Contents</strong></summary>",
        "",
    ]
    for title, anchor in toc_items:
        lines.append(f"- [{title}]({anchor})")
    lines.append("")
    lines.append("</details>")
    return "\n".join(lines)


